<script>
    function toggleFavorite(element) {
        // Check if the element is a button or an SVG
        const isButton = element.classList.contains('favbutton');
        const isSVG = element.tagName === 'svg';

        // Get the relevant elements based on the type
        const button = isButton ? element : element.closest('.favbutton');
        const svg = isSVG ? element : button.querySelector('svg');

        // Determine the template ID and current favorite status
        const templateId = button.closest('.template-item').getAttribute('data-template-id');
        const isFavorite = button.closest('.template-item').getAttribute('data-favorite');

        // Toggle favorite status
        const newFavoriteStatus = isFavorite === '1' ? '0' : '1';

        // Make AJAX request to update favorite status
        fetch(`/update-favorite-status/${templateId}/${newFavoriteStatus}`, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'X-CSRF-TOKEN': '{{ csrf_token() }}', // Include CSRF token if using Laravel CSRF protection
            },
        })
        .then(response => {
            if (response.ok) {
                return response.json();
            } else {
                throw new Error('Failed to update favorite status');
            }
        })
        .then(data => {
            // Update data-favorite attribute and button/SVG styling based on response
            button.closest('.template-item').setAttribute('data-favorite', newFavoriteStatus);
            if (newFavoriteStatus === '1') {
                button.classList.add('active');
                svg.querySelector('path').setAttribute('fill', '#E64242'); // Change SVG color to red
            } else {
                button.classList.remove('active');
                svg.querySelector('path').setAttribute('fill', ''); // Change SVG color to default
            }
        })
        .catch(error => {
            console.error('Error:', error);
        });
    }
</script>


